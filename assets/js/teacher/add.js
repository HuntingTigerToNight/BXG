
require(['/bxg/assets/js/config.js','/bxg/assets/js/common.js'],function(){
  require(['jquery','validate','datepicker','form','Chinese'],function($){
    //入职时间插件
    var options={
      format:'yyyy年mm月dd日',
      todayHighlight:true,
      language:'zh-CN'
    }
    $('input[name="tc_join_date"]').datepicker(options);
    //表单验证
    $('form').validate({
      submitHandler:function(){
  
        //验证后调用ajax方法（form表单里点击buton请求ajax已包含）
        $('form').ajaxSubmit({
          url:'/api/teacher/add',
          type:'post',
          success:function(info){
            if(info.code===200){
              console.log(info);
              window.location.href='/bxg/views/teacher/list.html';
            }
          }
        })
      },
      rules:{
        tc_name:{
          required:true,
          rangelength:[2,10],
        },
        tc_pass:{
          required:true,
          rangelength:[6,16],
        },
        tc_join_date:{
          required:true,
        }
      },
      messages:{
        tc_name:{
          required:'名字不能为空!',
          rangelength:'名字长度必须在2到5之间。'
        },
        tc_pass:{
          required:'密码不能为空!',
          rangelength:'密码长度为6到16个字母之间。',
        },
        tc_join_date:{
          required:'日期不能为空!'
        }
      }
    })
  })
})
